T = True
F = False
print(type(T))
print(type(F))

print (T and F)
print (T or F)
print (not F)
print (T & F)
print (T | F)
print (~T)

# Identity operators זהות בין משתנים -  is
X=2
Y=2
Z=30
print ("Identity operators")
print (X is Y)
print (X is Z)

# Membership operators -  האם איבק קיים בקבוצה in
myStr = "Hellow Blala"
myList = [1,2,4,5]
print ("Membership operators")
print ("H" in myStr)
print ("X" in myStr)
print ("X" not in myStr)
print (2 in myList) 


